export function translate(
  language: AppLanguage,
  key: string,
  params: Params = {},
) {
  const value = translations[language][key] ?? translations.es[key] ?? key;
  return typeof value === "function" ? value(params) : value;
}

export function getLocaleTag(language: AppLanguage) {
  return language === "es" ? "es-CO" : "en-US";
}

export function getWeekStartLabel(language: AppLanguage, weekStart: WeekStart) {
  if (language === "es") {
    return weekStart === "Sunday" ? "Domingo" : "Lunes";
  }
  return weekStart;
}

export function getPaymentMethodLabel(language: AppLanguage, method: string) {
  if (method === "cash") return translate(language, "paymentCash");
  if (method === "transfer") return translate(language, "paymentTransfer");
  return translate(language, "paymentCard");
}
